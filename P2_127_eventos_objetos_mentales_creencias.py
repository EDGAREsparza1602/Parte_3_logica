# Creamos un diccionario para representar creencias sobre objetos mentales o eventos
creencias = {
    "casa": "La casa está cerca del parque",
    "examen": "Creo que el examen será difícil",
    "proyecto": "Tengo confianza en que el proyecto será exitoso"
}

# Imprimimos las creencias
for objeto, creencia in creencias.items():
    print(f"Creencia sobre {objeto}: {creencia}")
